
const add = (num1, num2) => {
    return num1*1 + num2*2;
};

const subtract = (num1, num2) => {
    return num1*1 - num2*2;
};

const multiple = (num1, num2) => {
    return num1 * num2;
};

const divide = (num1, num2) => {
    return num1 / num2;
};



export default { add, subtract, multiple, divide }